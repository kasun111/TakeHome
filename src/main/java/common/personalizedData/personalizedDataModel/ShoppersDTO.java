package common.personalizedData.personalizedDataModel;

public class ShoppersDTO {

    private String shoppersId;
    private String name;
    private String contact;

    public String getShoppersId() {
        return shoppersId;
    }

    public void setShoppersId(String shoppersId) {
        this.shoppersId = shoppersId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }
}
